class Solution{
    public:
    int firstElementKTime(int a[], int n, int k)
    {
        unordered_map<int,int>temp;
        for(int i=0;i<n;i++)
        {
            int x=a[i];
            if(temp[x]==k-1)
            return x;
            temp[x]++;
        }
        return -1;
    }
};

