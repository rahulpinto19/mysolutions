class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        set<int>s;
        while(head2)
        {
            s.insert(head2->data);
            head2=head2->next;
        }
        Node* k=new Node(0);
        Node*ans=k;
        while(head1)
        {
            if(s.find(head1->data)!=s.end())
            {
                Node*temp=new Node(head1->data);
                ans->next=temp;
                ans=ans->next;
            }
            head1=head1->next;
        }
        Node*head=k->next;
        return head;
    }
};

